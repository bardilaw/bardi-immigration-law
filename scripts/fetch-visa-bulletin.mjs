#!/usr/bin/env node
// BAR-802 row 37 — DOS Visa Bulletin importer.
//
// The Department of State does NOT publish the Visa Bulletin as RSS/JSON, but the
// monthly page lives at a deterministic URL and renders plain HTML tables, so we
// fetch + parse at build time and write src/app/(en)/resources/visaBulletinData.ts.
// EN and ES resources pages both import that file, so they stay in parity.
//
// "Auto-update monthly" = run this on a schedule before each Cloudflare Pages build
// (a monthly Deploy Hook fired by a cron Worker). See deliverables/bar-802-*.md.
//
// Design rules:
//  - Never overwrite the committed data file with garbage. On ANY fetch/parse
//    failure we leave the existing file untouched and exit 0 so the build keeps
//    using the last-known-good data.
//  - Only Final Action Dates are imported (Family-Sponsored first, then
//    Employment-Based). No Diversity (DV) per the ticket.
//
// Usage: node scripts/fetch-visa-bulletin.mjs [YYYY-MM]

import { writeFileSync, readFileSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT_FILE = join(__dirname, '..', 'src', 'app', '(en)', 'resources', 'visaBulletinData.ts');

const MONTHS = [
  'january', 'february', 'march', 'april', 'may', 'june',
  'july', 'august', 'september', 'october', 'november', 'december',
];
const MONTHS_EN = ['January', 'February', 'March', 'April', 'May', 'June',
  'July', 'August', 'September', 'October', 'November', 'December'];
const MONTHS_ES = ['enero', 'febrero', 'marzo', 'abril', 'mayo', 'junio',
  'julio', 'agosto', 'septiembre', 'octubre', 'noviembre', 'diciembre'];

// Spanish labels for the fixed preference categories. Unknown/edge rows fall back
// to the raw English label so we never drop data we can't translate.
const ES_LABELS = {
  'F1': 'F1 — Hijos(as) solteros(as) de ciudadanos estadounidenses',
  'F2A': 'F2A — Cónyuges e hijos(as) menores de residentes permanentes',
  'F2B': 'F2B — Hijos(as) solteros(as) (21+) de residentes permanentes',
  'F3': 'F3 — Hijos(as) casados(as) de ciudadanos estadounidenses',
  'F4': 'F4 — Hermanos(as) de ciudadanos estadounidenses',
  '1st': '1ª — Trabajadores prioritarios',
  '2nd': '2ª — Profesionales con títulos avanzados / aptitud excepcional',
  '3rd': '3ª — Trabajadores calificados y profesionales',
  'Other Workers': 'Otros Trabajadores',
  '4th': '4ª — Ciertos inmigrantes especiales',
  'Certain Religious Workers': 'Ciertos Trabajadores Religiosos',
  '5th': '5ª — Inversionistas (EB-5)',
};

const URL_FOR = (y, mIdx) =>
  `https://travel.state.gov/content/travel/en/legal/visa-law0/visa-bulletin/${y}/visa-bulletin-for-${MONTHS[mIdx]}-${y}.html`;

function clean(s) {
  return s
    .replace(/<[^>]+>/g, ' ')
    .replace(/&nbsp;/gi, ' ')
    .replace(/&amp;/gi, '&')
    .replace(/\s+/g, ' ')
    .trim();
}

// Pull the first <table> that appears after a heading matching `needle`.
function tableAfter(html, needle) {
  const at = html.search(needle);
  if (at < 0) return null;
  const m = html.slice(at).match(/<table[\s\S]*?<\/table>/i);
  return m ? m[0] : null;
}

// Parse a final-action table into [{category, dates:{...}}]. A valid data row is
// exactly 6 cells: category + 5 chargeability areas.
function parseTable(tableHtml) {
  if (!tableHtml) return [];
  const rows = [];
  const trs = tableHtml.match(/<tr[\s\S]*?<\/tr>/gi) || [];
  for (const tr of trs) {
    const cells = (tr.match(/<t[dh][\s\S]*?<\/t[dh]>/gi) || []).map(clean);
    if (cells.length !== 6) continue;
    const cat = cells[0];
    // Skip header rows (no date-like / "C" values, or the column header row).
    const vals = cells.slice(1);
    const looksLikeData = vals.every((v) => /^(C|U|[0-9]{2}[A-Z]{3}[0-9]{2})$/.test(v));
    if (!looksLikeData) continue;
    rows.push({
      category: cat,
      dates: {
        allAreas: vals[0], china: vals[1], india: vals[2],
        mexico: vals[3], philippines: vals[4],
      },
    });
  }
  return rows;
}

function withEsLabels(rows) {
  return rows.map((r) => ({ ...r, categoryEs: ES_LABELS[r.category] || r.category }));
}

async function fetchHtml(url) {
  const res = await fetch(url, {
    headers: { 'User-Agent': 'Mozilla/5.0 (compatible; BardiLawBot/1.0; +https://bardilaw.com)' },
  });
  if (!res.ok) throw new Error(`HTTP ${res.status} for ${url}`);
  return res.text();
}

function render(data) {
  return `// AUTO-GENERATED by scripts/fetch-visa-bulletin.mjs — do not edit by hand.
// BAR-802 row 37: DOS Visa Bulletin (Final Action Dates). Family-Sponsored first,
// then Employment-Based. No Diversity (DV). Re-run the script to refresh monthly.

export interface VisaBulletinRow {
  /** Preference category code as published, e.g. "F2A" or "Other Workers". */
  category: string;
  /** Spanish label for the category. */
  categoryEs: string;
  /** Final Action Date per chargeability area ("C" = current, "U" = unavailable). */
  dates: {
    allAreas: string;
    china: string;
    india: string;
    mexico: string;
    philippines: string;
  };
}

export interface VisaBulletinData {
  /** Bulletin month, English, e.g. "June 2026". */
  monthLabel: string;
  /** Bulletin month, Spanish, e.g. "junio de 2026". */
  monthLabelEs: string;
  /** Source page on travel.state.gov. */
  sourceUrl: string;
  /** ISO date this data was imported. */
  fetchedAt: string;
  familyBased: VisaBulletinRow[];
  employmentBased: VisaBulletinRow[];
}

export const VISA_BULLETIN: VisaBulletinData = ${JSON.stringify(data, null, 2)};
`;
}

async function main() {
  const arg = process.argv[2];
  let year, mIdx;
  if (arg && /^\d{4}-\d{2}$/.test(arg)) {
    const [y, m] = arg.split('-').map(Number);
    year = y; mIdx = m - 1;
  } else {
    const now = new Date();
    year = now.getUTCFullYear(); mIdx = now.getUTCMonth();
  }

  // Try the target month, then walk back up to 2 months (the new bulletin is
  // posted mid-prior-month, so "this month" may not exist yet early on).
  const attempts = [];
  for (let back = 0; back < 3; back++) {
    let yy = year, mm = mIdx - back;
    while (mm < 0) { mm += 12; yy -= 1; }
    attempts.push([yy, mm]);
  }

  let html, used;
  for (const [yy, mm] of attempts) {
    try {
      html = await fetchHtml(URL_FOR(yy, mm));
      used = [yy, mm];
      break;
    } catch (e) {
      console.error(`  skip ${MONTHS[mm]} ${yy}: ${e.message}`);
    }
  }

  if (!html) {
    console.error('Visa Bulletin fetch failed for all attempts — keeping existing data file.');
    process.exit(0);
  }

  const fb = withEsLabels(parseTable(tableAfter(html, /FINAL ACTION DATES FOR[\s&nbsp;]*FAMILY/i)));
  const eb = withEsLabels(parseTable(tableAfter(html, /FINAL ACTION DATES FOR[\s&nbsp;]*EMPLOYMENT/i)));

  if (fb.length < 4 || eb.length < 4) {
    console.error(`Parse looked wrong (FB=${fb.length}, EB=${eb.length}) — keeping existing data file.`);
    process.exit(0);
  }

  const [yy, mm] = used;
  const data = {
    monthLabel: `${MONTHS_EN[mm]} ${yy}`,
    monthLabelEs: `${MONTHS_ES[mm]} de ${yy}`,
    sourceUrl: URL_FOR(yy, mm),
    fetchedAt: new Date().toISOString().slice(0, 10),
    familyBased: fb,
    employmentBased: eb,
  };

  const next = render(data);
  if (existsSync(OUT_FILE) && readFileSync(OUT_FILE, 'utf8') === next) {
    console.log(`Visa Bulletin already current (${data.monthLabel}).`);
    return;
  }
  writeFileSync(OUT_FILE, next);
  console.log(`Wrote ${data.monthLabel}: FB ${fb.length} rows, EB ${eb.length} rows -> ${OUT_FILE}`);
}

main().catch((e) => {
  // Last-resort guard: never fail the build over the bulletin importer.
  console.error('Visa Bulletin importer error (non-fatal):', e.message);
  process.exit(0);
});
